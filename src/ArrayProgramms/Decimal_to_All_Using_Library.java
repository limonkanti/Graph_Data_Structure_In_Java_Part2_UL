
package ArrayProgramms;


public class Decimal_to_All_Using_Library 
{
    public static void main(String args[])
    {
        //deci to hex;
        int a=45;
        String s=Integer.toHexString(a);
        System.out.println(s);   
        
        
        //deci to oc
        int oc=798;
        String ss=Integer.toOctalString(oc);
        System.out.println(ss);
        
        //deci ti bi
        int bi=15;
        String sb=Integer.toBinaryString(bi);
        System.out.println(sb);
           
    
    
    }
    
}
