
package list_LinkedList;

import java.util.LinkedList;


public class Traversing 
{
    
    public static void main(String args[])
    {
        LinkedList<Integer> li=new LinkedList<Integer>();
        li.add(17);
        li.add(22);
        
        System.out.println(li);
        li.remove();
        System.out.println(li);
    }       
            
}
